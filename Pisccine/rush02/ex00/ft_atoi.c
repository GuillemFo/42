#include <stdio.h>

int	atoi(const char* D)
{
	int	num; 
	int	i;

	num = 0; 
	i = 0;
	while (D[i] && (D[i] >= '0' && D[i] <= '9'))
	{
		num = num * 10 + (D[i] - '0');
		i++;
	}
	return (num);
}
